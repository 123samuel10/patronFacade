package org.example.model;
//Clase SistemaSeguridad: Representa el sistema de seguridad del hogar.
public class SistemaSeguridad {


    //Método activarModoNocturno: Activa el modo nocturno, que podría incluir la activación de alarmas o cámaras.

    public void activarModoNocturno() {
        System.out.println("Sistema de seguridad: Modo nocturno activado.");
    }
    //Método desactivarSeguridad: Desactiva el sistema de seguridad.
    public void desactivarSeguridad() {
        System.out.println("Sistema de seguridad: Desactivado.");
    }
}
